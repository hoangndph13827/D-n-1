    private void fillTableHoaDon() {
        try {
            List<HoaDonModel> lst = hdDao.select();
            DefaultTableModel model = (DefaultTableModel) tbHD.getModel();
            model.setRowCount(0);
            lstHD.clear();
            for (HoaDonModel x : lst) {
                model.addRow(new Object[]{
                    x.getNgayMua(), x.getId(), x.getThanhTien(),  x.getUser()
                });
                Vector vt= new Vector();
                List<CTHoaDonModel> cthd= cthdDao.findByIdHD(x.getId());
                vt.add(x);
                vt.add(cthd);
                List<CTSachModel> cts= new ArrayList<>();
                for(CTHoaDonModel a:cthd) cts.add(ctsDao.findById(a.getId_CTsach()));
                vt.add(cts);
                lstHD.add(vt);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

