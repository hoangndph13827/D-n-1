    private void fillTableChiTietHoaDon() {
       try {
            Vector vt= lstHD.get(tbHD.getSelectedRow());
            List<CTSachModel> s = (List<CTSachModel>) vt.get(2);
            int index = 0;
            pnSP.setSize(s.size() * 160, 160);
            pnSP.removeAll();
            pnSP.setLayout(new GridLayout(1, s.size()));
            for (int i = 0; i < s.size(); i++) {
                CTSachModel smod;
                try {
                    smod = s.get(index);
                    SanPham pn = new SanPham(smod);
                    pn.lbAdd.setVisible(false);
                    pn.setSize((int) pnSP.getPreferredSize().getWidth() / 5, 0);
                    pnSP.add(pn);
                    index++;
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            for (int i = s.size(); i < 3; i++) {
                JPanel pal= new JPanel();
                pal.setOpaque(false);
                pnSP.add(pal);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

